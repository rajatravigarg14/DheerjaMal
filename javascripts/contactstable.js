//Comparer Function  
function GetSortOrder(prop) {  
    return function(a, b) {  
        if (a[prop] > b[prop]) {  
            return 1;  
        } else if (a[prop] < b[prop]) {  
            return -1;  
        }  
        return 0;  
    }  
}  

contactslist.contacts.sort(GetSortOrder("name"));

// Find a <table> element with id="contactstable":
var table = document.getElementById("contactstable");

for (var i = 0; i < contactslist.contacts.length; i++) {
    // Create an empty <tr> element and add it to the 1st position of the table:
    var row = table.insertRow(i+1);

    // Insert new cells (<td> elements) at the 1st and 2nd position of the "new" <tr> element:
    var cell1 = row.insertCell(0);
    var cell2 = row.insertCell(1);
    var cell3 = row.insertCell(2);

    // Add some text to the new cells:
    cell1.innerHTML = i + 1;
    cell2.innerHTML = contactslist.contacts[i].name;
    cell3.innerHTML = contactslist.contacts[i].contact;
}


function searchFunction() {
    // Declare variables 
    var input, filter, tr, td, i;
    input = document.getElementById("searchInput");
    filter = input.value.toUpperCase();
    tr = table.getElementsByTagName("tr");
  
    // Loop through all table rows, and hide those who don't match the search query
    for (i = 1; i < tr.length; i++) {
      td = tr[i].getElementsByTagName("td")[1];
      if (td) {
        if (td.innerHTML.toUpperCase().indexOf(filter) > -1) {
          tr[i].style.display = "";
        } else {
          tr[i].style.display = "none";
        }
      } 
    }
  }